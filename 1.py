class math1:
    def plus(self,a,b):
        print(a+b, "- Сложение")
    def minus(self,a,b):
        print( a-b, "- Вычитание")
    def umnojenie(self,a,b):
        print(a*b, "- Умножение")
    def delenie(self,a,b):
        print(a/b, "- Деление")
    def stepen(self,a,b):
        print(a**b, "- Квадрат")
matem = math1()
matem.plus(5,3)
matem.minus(5,3)
matem.umnojenie(5,3)
matem.delenie(5,3)
matem.stepen(5,3)
 
